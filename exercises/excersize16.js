// Практика создания функциональных выражений
// Создайте функцию (как функциональное выражение - function expression), которая принимает 4 числа и возвращает их среднее арифметическое.
// Сохраните функцию в переменную average.

const average = function (n1, n2, n3, n4) {
  return (n1 + n2 + n3 + n4) / 4
}
